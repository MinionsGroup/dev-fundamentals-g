package contactManager;

class Contact{
	   String name;
	   String phoneNumber;
	  
	   public void setphoneNumber(String phone) {
		   phoneNumber = phone;
	   }
	   
	   public void setName(String name) {
		   this.name = name;
	   }
	   
	   public String getphoneNumber() {
		   return phoneNumber;
	   }
	   
	   public String getName() {
		   return name;
	   }
}
